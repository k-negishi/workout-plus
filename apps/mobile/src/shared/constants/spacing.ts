/**
 * スペーシングトークン
 * 4pxグリッドベースの一貫したスペーシング定義
 */
export const spacing = {
  /** 4px - 最小の余白（アイコンとテキスト間など） */
  xs: 4,
  /** 8px - 小さな余白（リスト項目内パディング） */
  sm: 8,
  /** 16px - 標準余白（カード内パディング） */
  md: 16,
  /** 24px - 大きめの余白（セクション間） */
  lg: 24,
  /** 32px - 大きな余白（画面内の主要セクション間） */
  xl: 32,
  /** 48px - 最大余白（画面上下の余白） */
  xxl: 48,
} as const;

/** スペーシングの型 */
export type SpacingKey = keyof typeof spacing;
